package com.neobrutalism.crm.domain.branch;

/**
 * Branch Status Enum
 */
public enum BranchStatus {
    /**
     * ACTIVE - Branch đang hoạt động
     */
    ACTIVE,

    /**
     * INACTIVE - Branch tạm ngưng hoạt động
     */
    INACTIVE,

    /**
     * CLOSED - Branch đã đóng cửa
     */
    CLOSED
}
